package com.BulkSMS;

public class StructContact {
	 String Name;
	 String NumberPhone;
	StructContact(){}
	public String GetName()
	{
		return Name;
	}
	public String GetNumberPhone()
	{
		return NumberPhone;
	}
	public void SetName(String PName)
	{
		Name = PName;
	}
	public void SetNumberPhone(String PNumberPhone)
	{
		NumberPhone = PNumberPhone;
	}
}
